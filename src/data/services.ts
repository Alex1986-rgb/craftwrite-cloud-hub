// Функции для фильтрации и поиска
export const getServicesByCategory = (category: string): Service[] => {
  if (category === "all") return SERVICES;
  return SERVICES.filter(service => service.category === category);
};

export const getServicesByFormat = (format: string): Service[] => {
  if (format === "all") return SERVICES;
  return SERVICES.filter(service => service.format === format);
};

export const searchServices = (query: string): Service[] => {
  const lowercaseQuery = query.toLowerCase();
  return SERVICES.filter(service => 
    service.name.toLowerCase().includes(lowercaseQuery) ||
    service.desc.toLowerCase().includes(lowercaseQuery) ||
    service.detail.toLowerCase().includes(lowercaseQuery)
  );
};

// Получение уникальных категорий, форматов, языков и тем
export const getUniqueCategories = (): string[] => {
  return [...new Set(SERVICES.map(service => service.category))];
};

export const getUniqueFormats = (): string[] => {
  return [...new Set(SERVICES.map(service => service.format))];
};

export const getUniqueLanguages = (): string[] => {
  return [...new Set(SERVICES.map(service => service.lang))];
};

export const getUniqueTopics = (): string[] => {
  return [...new Set(SERVICES.map(service => service.topic))];
};
