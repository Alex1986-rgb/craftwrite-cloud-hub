export const getServiceBySlug = (slug: string): Service | undefined => {
  return SERVICES.find(service => service.slug === slug);
};

export const getServicesByCategory = (category: string): Service[] => {
  return SERVICES.filter(service => service.category === category);
};

export const getPopularServices = (): Service[] => {
  return SERVICES.filter(service => service.popularity >= 4).sort((a, b) => b.popularity - a.popularity);
};

export const getServicesByDifficulty = (difficulty: Service['difficulty']): Service[] => {
  return SERVICES.filter(service => service.difficulty === difficulty);
};
